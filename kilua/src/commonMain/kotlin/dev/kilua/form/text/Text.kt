package dev.kilua.form.text

import androidx.compose.runtime.Composable
import androidx.compose.runtime.DisposableEffect
import androidx.compose.runtime.remember
import dev.kilua.compose.ComponentNode
import dev.kilua.core.ComponentBase
import dev.kilua.core.DefaultRenderConfig
import dev.kilua.core.RenderConfig
import dev.kilua.form.Input
import dev.kilua.form.InputType
import dev.kilua.form.StringFormControl

public open class Text(
    value: String? = null,
    type: InputType = InputType.Text,
    name: String? = null,
    maxlength: Int? = null,
    placeholder: String? = null,
    disabled: Boolean? = null,
    className: String? = null,
    renderConfig: RenderConfig = DefaultRenderConfig()
) : Input<String>(value, type, name, maxlength, placeholder, disabled, className, renderConfig), StringFormControl {

    override fun setValueFromString(text: String?) {
        value = if (text.isNullOrEmpty()) {
            null
        } else {
            text
        }
    }

}

@Composable
public fun ComponentBase.text(
    value: String? = null,
    type: InputType = InputType.Text,
    name: String? = null,
    maxlength: Int? = null,
    placeholder: String? = null,
    disabled: Boolean? = null,
    className: String? = null,
    content: @Composable Text.() -> Unit = {}
): Text {
    val component = remember { Text(value, type, name, maxlength, placeholder, disabled, className, renderConfig) }
    DisposableEffect(component.componentId) {
        component.onInsert()
        onDispose {
            component.onRemove()
        }
    }
    ComponentNode(component, {
        set(value) { updateProperty(Text::value, it) }
        set(type) { updateProperty(Text::type, it) }
        set(name) { updateProperty(Text::name, it) }
        set(maxlength) { updateProperty(Text::maxlength, it) }
        set(placeholder) { updateProperty(Text::placeholder, it) }
        set(disabled) { updateProperty(Text::disabled, it) }
        set(className) { updateProperty(Text::className, it) }
    }, content)
    return component
}
